import { createSlice } from "@reduxjs/toolkit";
import { initialUserStateType } from "../type";

const initialState: initialUserStateType = {
  user: null,
};

export const userSlice = createSlice({
  name: "user",
  initialState,
  reducers: {
    login: (state, action) => {
      state.user = action.payload;
    },
    logout: (state) => {
      state.user = null;
    },
  },
});
